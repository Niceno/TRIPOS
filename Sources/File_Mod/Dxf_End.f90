!==============================================================================!
  subroutine File_Mod_Dxf_End
!------------------------------------------------------------------------------!
  implicit none
!------------------------------------------------------------------------------!
!   Subroutine to end a .dxf file.                                             !
!==============================================================================!

  write(FU, "(a)")  "0"
  write(FU, "(a)")  "ENDSEC"
  write(FU, "(a)")  "0"
  write(FU, "(a)")  "EOF"

  close(FU)

  end subroutine
