!==============================================================================!
  real(RP) function Mesh_Mod_Area(na, nb, nc)
!------------------------------------------------------------------------------!
  implicit none
!------------------------------------------------------------------------------!
  type(Node_Type) :: na, nb, nc
!==============================================================================!

  Mesh_Mod_Area = ( (nb % x - na % x) * (nc % y - na % y)    &
                  - (nb % y - na % y) * (nc % x - na % x) )

  end function
