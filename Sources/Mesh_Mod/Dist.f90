!==============================================================================!
  real(RP) function Mesh_Mod_Dist(na, nb)
!------------------------------------------------------------------------------!
  implicit none
!------------------------------------------------------------------------------!
  type(Node_Type) :: na, nb
!==============================================================================!

  Mesh_Mod_Dist = sqrt(   (nb % x - na % x)**2    &
                        + (nb % y - na % y)**2 )

  end function
